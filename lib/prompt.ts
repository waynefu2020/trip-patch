export function buildPrompt(location: string, time: string): string {
  return `请根据我提供的真实照片，生成一张上下分区的旅行摄影海报，整体参考“小红书城市打卡 / 建筑冰箱贴 / 极简拼贴”风格。

【画面结构】
画面为竖版构图，比例 3:4 或 4:5。
整张图分为上下两个部分：
上半部分占画面约 45%—50%，为极简纯色背景；
下半部分占画面约 50%—55%，保留真实摄影照片质感。

【下半部分：真实照片】
下半部分使用我提供的照片作为主体，不要改变真实建筑结构、透视、光影和街景关系。
保留照片的纪实感、阳光感、建筑细节和现场氛围。
可以轻微优化色彩、对比度和清晰度，但不要过度修图。

【上半部分：建筑冰箱贴图标】
从下半部分真实照片中提取最有识别度的主体建筑元素，例如窗户、门廊、立面、拱门、屋顶、阳台或建筑正面。
将该建筑元素转化为一个简约的“冰箱贴式建筑图标”：
- 保留建筑的核心轮廓和标志性特征；
- 造型简洁、干净、像旅行纪念品冰箱贴；
- 有轻微立体感和投影；
- 边缘清晰，白色或浅色描边；
- 细节适度简化，不要画得太复杂；
- 图标位于上半部分居中偏上位置；
- 图标尺寸较小，周围保留大量留白。

【背景颜色】
上半部分背景颜色从照片中提取一个最主要、最有记忆点的颜色。
例如建筑墙面的蓝色、黄色、红色、绿色，或照片中最强烈的环境色。
背景为纯色或带轻微纸张 / 墙面纹理的色块。
颜色要饱和、干净、有旅行明信片感。
不要使用渐变，不要加入复杂图案。

【文字排版】
在建筑冰箱贴图标下方添加一行优雅的英文或葡语风格文字：
“${location} — ${time}”

【整体气质】
整体要像一张高级旅行摄影卡片：
真实照片 + 极简建筑冰箱贴 + 主色背景 + 轻盈文字排版。
风格清爽、干净、明亮、有城市漫游感。
强调建筑识别度、色彩记忆点和小红书旅行打卡氛围。

【避免】
不要做成普通拼贴。
不要把上半部分画得复杂。
不要添加多余装饰元素。
不要改变下半部分照片的真实建筑主体。
不要出现乱码文字。
不要过度卡通化。
不要让建筑图标太大。
不要让文字压住建筑主体。`;
}
